export const isTokenValid = (expiresAt?: number): boolean => {
  if (!expiresAt) return false;
  
  const now = Math.floor(Date.now() / 1000);
  const buffer = 60; // 1 –º–∏–Ω—É—Ç–∞ –±—É—Ñ–µ—Ä–∞
  
  return expiresAt > (now + buffer);
};

